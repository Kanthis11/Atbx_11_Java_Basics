package Operators;

public class Comparison {
    public static void main(String[] args) {
        // == ?  Compare the values
        System.out.println( 20 == 20);
        System.out.println( 20 < 10);
        System.out.println( 90 == 20);
        System.out.println( 10 >= 10);


        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
