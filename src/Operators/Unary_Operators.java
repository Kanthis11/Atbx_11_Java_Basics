package Operators;

public class Unary_Operators {
    public static void main(String[] args) {
        // Unary Operator
        int a = +20;
        int a1 = -180;
        int result = a+a1;
        System.out.println(a);
        System.out.println(a1);
        System.out.println(result);


        int b = -1;
        b = b+1;
        System.out.println(b);

    }
}
