package Operators;

public class ASCII {
    public static void main(String[] args) {
        char a1 = 'A'; // ASCII -> 65
        char b1 = 'B'; // 66
        System.out.println(a1 + b1);
        // a1+b1 - 65+66 -> 131


        short s = 10;
        char c = 'A';//65
        System.out.println(c+s);
    }
}
